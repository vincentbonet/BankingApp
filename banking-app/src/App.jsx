import { BrowserRouter as Router, Switch, Route } from 'react-router-dom';
import './App.css';

function Home() {
  return <h1>Home Component</h1>;
}

function About() {
  return <h1>About Component</h1>;
}

function App() {
  return (
    <Router>
      <div>
        <nav>
          <ul>
            <li>
              <Link to="/">Home</Link>
            </li>
            <li>
              <Link to="/about">About</Link>
            </li>
          </ul>
        </nav>

        <Switch>
          <Route path="/about">
            <About />
          </Route>
          <Route path="/">
            <Home />
          </Route>
        </Switch>
      </div>
    </Router>
  );
}

export default App;
